# Control Statements -> Loop

# 3) Statements

# 1) break - Stops Loop Immediately 
# 2) continue - skips the particular Condition
# 3) pass - future coding PY

for i in range(1,11):
    print(i)
    if(i==5):
        break

for i in range(1,11):
    if(i==5):
        continue
    print(i)

for i in range(1,11):
    pass