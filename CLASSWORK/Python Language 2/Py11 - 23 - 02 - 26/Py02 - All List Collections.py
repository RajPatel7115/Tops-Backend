n = [10, 10, 20, 30, 90, 100, "string", "int", "loop", True, False]

n.append(100)   # Add value at end of list
print(n)

n.clear()       # Clears Whole List
print(n)

n.copy()        # Copy the list
print(n)

n.count()       # Count total values
print(n)

n.extend(300,400)      # Adds multiple values in list
print(n)

n.index(4)       # Select Specific Index
print(n)

n.insert(4,"ok")      # insert value at specific index
print(n)

n.pop()         # Removes Last value from list 
print(n)

n.remove("ok")      # Remove Specific Word/num
print(n)

n.sort()        # Only Work With One Data Type
print(n)

