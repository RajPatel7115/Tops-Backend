s = "Python Programming"
print(type(s))

print(len(s)) # To Get Length Of Code
print(s.capitalize()) # To Capitalize First Letter
print(s.casefold()) # To Convert Into Lowercase
print(s.lower()) # To Convert Into Lowercase
print(s.upper()) # To Convert Into Uppercase
print(s.center(25,"*")) # To Center The String
print(s.find("P",2)) # To Find The Index Of Character
print(s.isalpha()) # To Check If All Characters Are Alphabets
print(s.isalnum()) # To Check If All Characters Are Alphabets & numeric
print(s.replace("P","T")) # To Replace Any Charcter To Given
print(s.swapcase()) # To Change Capital To Small
